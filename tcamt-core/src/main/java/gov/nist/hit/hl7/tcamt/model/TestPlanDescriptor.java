package gov.nist.hit.hl7.tcamt.model;

public class TestPlanDescriptor {
    String id;
    String name;

    public TestPlanDescriptor() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
